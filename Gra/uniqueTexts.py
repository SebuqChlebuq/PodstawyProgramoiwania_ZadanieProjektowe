def bigNumbers(text, x, y):
    textLenght = 0
    for i in text:
        if i == '0':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"█▀█")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"█▄█")
        elif i == '1':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"▄█")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H'," █")
        elif i == '2':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"▀█")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"█▄")# 3 sie nie da
        elif i == '4':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"█ █")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"▀▀█")
        elif i == '5':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"█▀")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"▄█")
        elif i == '6':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"█▄▄")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"█▄█")
        elif i == '7':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"▀▀█")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"  █")# 8 sie nie da
        elif i == '9':         
            print('\033['+str( x )+';'+str(y + textLenght*4)+'H',"█▀█")
            print('\033['+str(x+1)+';'+str(y + textLenght*4)+'H',"▀▀█")
        textLenght+=1


